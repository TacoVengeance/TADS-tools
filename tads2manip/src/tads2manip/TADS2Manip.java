    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File("."));
        fc.setAcceptAllFileFilterUsed(false);
        fc.setMultiSelectionEnabled(false);
        fc.setFileFilter(new FileNameExtensionFilter("TADS games (*.gam)", "gam"));
        int ret = fc.showOpenDialog(this);
        if(ret == JFileChooser.APPROVE_OPTION) {
            txtInput.setText(fc.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_btnInputActionPerformed

    private void btnPatchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPatchActionPerformed
        btnPatch.setEnabled(false);
        TADS2patcherHelper patcher = new TADS2patcherHelper();
        patcher.in = txtInput.getText();
        patcher.out = txtOutput.getText();
        patcher.pfile = patchList[cmbPatchFile.getSelectedIndex()].getAbsolutePath();
        patcher.log = txtLog;
        patcher.btn = btnPatch;
        patcher.verbose = false;
        new Thread(patcher).start();
        //btnPatch.setEnabled(true);
    }//GEN-LAST:event_btnPatchActionPerformed

