    public TADS2Manip() {
        //Prepare to get patch list
        File patchFolder = new File("patches");
        if(!patchFolder.exists() || !patchFolder.isDirectory())
            System.err.println("Error: patches directory does not exist.");
        
        //Get list of patch files
        patchList = patchFolder.listFiles(new FilenameFilter() {
            public boolean accept(File dir, String name) {
                return name.toLowerCase().endsWith(".patch");
            }
        });
        
        //Get names from patch files
        try {
            patchTitleList = new String[patchList.length];
            for(int x = 0; x < patchList.length; x++) {
                FileReader fr = new FileReader(patchList[x]);
                BufferedReader br = new BufferedReader(fr);
                String line = br.readLine();
                if(line != null && line.startsWith("#")) {
                    patchTitleList[x] = line.substring(1);
                } else {
                    patchTitleList[x] = patchList[x].getName();
                }
                br.close();
                fr.close();
            }
        } catch(Exception e) {
            System.err.println("Error reading patch list & titles.");
            patchTitleList = new String[0];
            System.err.println(e.toString());
        }
        
        //Start auto-init of GUI
        initComponents();
        
        txtInput.getDocument().addDocumentListener(new DocumentListener() {
        public void changedUpdate(DocumentEvent e) {
            txtInputChanged();
        }
        public void removeUpdate(DocumentEvent e) {
            txtInputChanged();
        }
        public void insertUpdate(DocumentEvent e) {
            txtInputChanged();
        }

        public void txtInputChanged() {
            String str = txtInput.getText();
            if(str.equals("")) {
                str = "(Patched Game Location)";
            } else if(str.indexOf(".gam") >= 0) {
                str = str.replaceFirst(".gam", ".patched.gam");
            } else {
                str = str.concat(".patched.gam");
            }
            txtOutput.setText(str);
        }
        });
    }

